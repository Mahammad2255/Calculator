using System;

namespace ClassLibrary1
{
    public class Calculator
    {
        
        public int Ustegel(int num1, int num2)
        {
            return num1 + num2;
        }
        public int Cix(int num1, int num2)
        {
            return num1 - num2;
        }
        public int Bol(int num1, int num2)
        {
            return num1 / num2;
        }
        public int Vur(int num1, int num2)
        {
            return num1 * num2;
        }


    }
}
